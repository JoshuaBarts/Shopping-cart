    make sure your basket is saved during all the steps of your checkout process). These cookies are required to enable core site functionality. 
    These cookies are stored for the duration of your browsing session.</p> 
    <p><b>26.	Functionality and Analytics Cookies:</b> are included so that we can give you an even smoother customer experience (i.e. these cookies 
    help you to save and remind you about your shopping bag and enable you to create a wish list). These cookies allow us to analyze site 
    usage so we can measure and improve performance. These cookies may be placed by us or a third party on our behalf (see the “opt out” 
    options provided below) and are stored for the duration of your browsing session.</p> 
    <p>The web analytic services on our website and mobile applications are provided by our agent, Adobe Site Catalyst (www.adobe.com). 
    This means that when you visit our website or mobile applications, a cookie by Adobe Site Catalyst will be stored on your computer or 
    mobile device, except when your browser settings don’t allow for such cookies. This further means that when you visit our website or 

    <h3>WHAT YOU SHOULD KNOW ABOUT COOKIES</h3>
    
    <h4>FIRST OF ALL…WHAT ARE COOKIES?</h4>
    Cookies are small text files that are stored on your computer or mobile device when you visit a website. For this section, we conveniently 
    use 'cookies' as an umbrella term for techniques such as cookies, Flash cookies and web beacons. They don’t take much space and they will 
    be removed automatically when expired. Some cookies expire at the end of your internet session, while others will be saved for a limited 
    amount of time.
    
    <h4>SECONDLY…WHAT ARE THE DIFFERENT COOKIE TYPES WE USE?</h4>
    
    
    <p>There are different types of cookies with different uses. Some exist just to allow you to browse the website and see certain features. 
    Some give us an idea of your browsing experience (i.e. if you have trouble finding what you are looking for), so that we can improve our 
    website’s functionality and make your future visit as pleasant as possible.</p>
    <p>Cookies set by JMB are called <b>'first party cookies'</b>. Cookies set by parties other than us are called 'third party cookies'. Third party 
    cookies enable third party features or functionality to be provided on or through JMB’ website (e.g. advertising, interactive content 
    and analytics). The parties that set these third-party cookies can recognize your computer or mobile device, (i) when it visits the website 
    in question and (ii) when it visits certain other websites.
    Another important distinction is <b>“required cookies”</b> vs. <b>“functionality cookies”</b> vs. <b>“advertising and social media cookies”</b>. Required 
    cookies are essential to running a website and help you to navigate on the website and use its basic features, including media plugins. 
    Then there are functionality cookies, which enable you to save your shopping bag, create a wish list and to save your shipping details 
    for a quicker checkout. These performance and analytics cookies collect information about how visitors use our website and used to improve 
    our website. We also like to show you relevant offers. To manage this we collect browsing data, which may be linked to a unique identifier 
    to allow us to understand how you interact with JMB across different platforms as described in our privacy statement (click here for our 
    privacy statement). Advertising and social media cookies capture your preferences in order to show you relevant ads outside of JMB sites. 
    These interaction cookies are used for you to interact with social media or to submit reviews and may be used to track your activity over 
    social media platforms.</p> 
    
